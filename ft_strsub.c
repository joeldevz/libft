#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*sub;
	size_t	a;

	a = 0;
	if (!s || !(sub = ft_strnew(len)))
		return (NULL);
	while (a < len)
	{
		sub[a] = s[start + a];
		a++;
	}
	return (sub);
}
