#include "libft.h"

void	ft_putnbr(int n)
{
	(n < 0 ? ft_putchar('-') : 1);
	n *= (n > 0 ? -1 : 1);
	(n <= -10 ? ft_putnbr(-(n / 10)) : 1);
	ft_putchar('0' - n % 10);
}
