#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int	a;

	a = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[a] == s2[a])
	{
		if (s1[a] == '\0' && s2[a] == '\0')
			return (1);
		a++;
	}
	return (0);
}
